#include <stdio.h>

int main(){

    int num1 = 2;
    long num2 = 1000000;
    float numFloat = 23.2;
    double numDouble = 35.5;

    printf("El siguiente numero entero es: %i \n", num1);
    printf("El siguiente numero Long es: %lu \n", num2);
    printf("El siguiente numero Float es: %f \n", numFloat);
    printf("El siguiente numero Double es: %lf \n", numDouble);      

    return 0;
}